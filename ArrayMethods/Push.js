//Making Array
let arr = ["Rohaan", "Nadeem"];

//push
console.log(arr.push("Rohaan")); //this will add the value at the last index of the array
console.log(arr);
//[ 'Rohaan', 'Nadeem', 'Rohaan' ]

//push
console.log(arr.push(1)); //this will add the value at the last index of the array
console.log(arr);
//[ 'Rohaan', 'Nadeem', 'Rohaan', 1]

//push
console.log(arr.push("Hafsa")); //this will add the value at the last index of the array
console.log(arr);
//[ 'Rohaan', 'Nadeem', 'Rohaan', 1, 'Hafsa' ]